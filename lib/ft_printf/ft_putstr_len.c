#include "ft_printf.h"

void	ft_putstr_len(char *s, int *len)
{
	size_t	i;

	i = 0;
	if (s == NULL)
	{
		write(1, "(null)", 6);
		*len += 6;
	}
	else
	{
		while (s[i])
		{
			write(1, &s[i], 1);
			*len += 1;
			i++;
		}
	}
}
